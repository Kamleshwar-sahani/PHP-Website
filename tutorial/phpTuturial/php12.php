                            <li><span>Opening a File:</span>To work with a file, you need to open it first. You can use the fopen() function to open a file and obtain a file handle, </li>
                            which you can use for subsequent operations. For example:
                            <div class="codeArea" style="height: 300px;">
                                <span class="mdi mdi-content-copy" onclick="example1()"></span>
