                                        $handle = fopen($filename, "a"); // Opens the file in append mode
                                        
                                        $content = "Appended content";
                                        
                                        // Append content using fwrite()
                                        fwrite($handle, $content);
                                        
                                        // Append content using file_put_contents()
                                        file_put_contents($filename, $content, FILE_APPEND);
                                        
                                        fclose($handle);
                                        
                                        
                                        
            
