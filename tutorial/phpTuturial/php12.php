                    <h1> file inclusion </h1>
                </div>
                <div class="defination">
                    <h4>In PHP, file inclusion is the process of including the contents of one PHP file within another PHP file. It allows you to reuse code, organize your code into modular components, and separate concerns.</h4>
                    <h4>There are two main ways to include files in PHP: include and require. Both methods accomplish the same goal of including the content of another file, but they differ in how they handle errors:</h4>
                    <h4>
                        <ol>
                            <li>include: The include statement includes and evaluates the specified file. If the file is not found or there is an error during inclusion, a warning is generated, but the script execution continues.</li>
                            example:
                            <div class="codeArea" style="height: 300px;">
                                <span class="mdi mdi-content-copy" onclick="example6()"></span>
                                <textarea id="codeArea" class="example6" readonly>

                                    <?php
                                    include 'header.php'; // Includes the content of header.php file
                                    // Rest of the code
                                    ?>
                                    
                                    
                                    
                                    
        
                                     </textarea>
                            </div>

                            <li>require: The require statement also includes and evaluates the specified file, but if the file is not found or there is an error, a fatal error is generated, and the script execution stops.</li>
                            Example:
                            <div class="codeArea" style="height: 300px;">
                                <span class="mdi mdi-content-copy" onclick="example7()"></span>
                                <textarea id="codeArea" class="example7" readonly>


                                    <?php
                                    require 'config.php'; // Includes the content of config.php file
                                    // Rest of the code
                                    ?>
                                    
                                    
                                    
                                    
                                    
        
                                     </textarea>
                            </div>
                            
                        </ol>
                    </h4>
                    <h4>
                        In addition to include and require, there are variants with an additional _once suffix: include_once and require_once. These functions ensure that a file is included only once to prevent duplicate inclusion and potential redeclaration of functions or classes.
                    </h4>
                    <h4>It's important to note that when including files, the file path can be specified as an absolute path or a relative path. If the file you want to include is in the same directory as the current script, you can simply specify the filename. Otherwise, you may need to provide the relative or absolute path to the file.</h4>
                    <h4>Example with relative path:</h4>
                    <div class="codeArea" style="height: 300px;">
                        <span class="mdi mdi-content-copy" onclick="example8()"></span>
                        <textarea id="codeArea" class="example8" readonly>


                            <?php
                            include 'includes/header.php'; // Includes the content of header.php in the includes directory
                            // Rest of the code
                            ?>
                            
                            
                            
                            
                            

                             </textarea>
                    </div>
                    <h4>Example with absolute path:</h4>
                    <div class="codeArea" style="height: 300px;">
                        <span class="mdi mdi-content-copy" onclick="example9()"></span>
                        <textarea id="codeArea" class="example9" readonly>

                            <?php
                            require '/var/www/html/config.php'; // Includes the content of config.php with an absolute path
                            // Rest of the code
                            ?>
                            
                            
                            
                            
                            
                            

                             </textarea>
                    </div>
                    <h4>File inclusion is a powerful feature in PHP that allows you to build modular and maintainable code by reusing and organizing your codebase.</h4>
