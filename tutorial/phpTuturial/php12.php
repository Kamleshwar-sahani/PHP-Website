                            <li> <span>Reading File Content:</span>To read the content of a file, you can use functions like fread() or fgets(). fread() allows you to read a specific number of bytes, while fgets() reads one line at a time.</li>
                            Here's an example:


                            <div class="codeArea">
                                <span class="mdi mdi-content-copy" onclick="example2()"></span>
                                <textarea id="codeArea" class="example2" readonly>

                                    $filename = "example.txt";
                                    $handle = fopen($filename, "r");
                                    
                                    // Read the entire file
                                    $content = fread($handle, filesize($filename));
                                    
                                    // Read line by line
                                    while (($line = fgets($handle)) !== false) {
                                        // Process each line
                                    }
                                    
                                    fclose($handle);
                                    
        
                                     </textarea>
                            </div>

                            <li> <span>Writing to a File:</span>To write content to a file, you can use functions like fwrite() or file_put_contents()</li>
                            Here's an example:
                            <div class="codeArea">
                                <span class="mdi mdi-content-copy" onclick="example3()"></span>
                                <textarea id="codeArea" class="example3" readonly>

                                    $filename = "example.txt";
                                    $handle = fopen($filename, "w"); // Opens the file in write mode
                                    
                                    $content = "Hello, World!";
                                    
                                    // Write content using fwrite()
                                    fwrite($handle, $content);
                                    
                                    // Write content using file_put_contents()
                                    file_put_contents($filename, $content);
                                    
                                    fclose($handle);
                                    
                                    
        
                                     </textarea>
                            </div>
                            <li><span>Appending to a File:</span>
                                If you want to append content to an existing file without overwriting the existing content, you can use the "a" mode with fopen() or file_put_contents().</li>
                                Here's an example:
                                <div class="codeArea">
                                    <span class="mdi mdi-content-copy" onclick="example4()"></span>
